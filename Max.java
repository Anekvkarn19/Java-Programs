public class Max 
{
    public static void main(String[] args) 
    {
        int num1 = 15;
        int num2 = 25;
        int max;
        
        if(num1>num2)
        {
             max = num1;
        }
        else
        {
             max = num2;
        }
        System.out.println("The maximum of " + num1 + " and " + num2 + " is: " + max);
    }
}
